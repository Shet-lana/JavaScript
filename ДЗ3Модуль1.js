// Задание 1: Подсчитать сумму всех чисел в заданном пользователем диапазоне

// Используем цикл for, так как диапазон известен заранее
let start = +prompt("Введите начальное значение диапазона:");
let end = +prompt("Введите конечное значение диапазона:");
let sum = 0;
for (let i = start; i <= end; i++) {
    sum += i;
}
console.log(`Сумма чисел в диапазоне от ${start} до ${end}:`, sum);


// Задание 2: Запросить 2 числа и найти только наибольший общий делитель

// Используем цикл while, так как мы не знаем заранее, когда завершится процесс нахождения НОД
let a = +prompt("Введите первое число:");
let b = +prompt("Введите второе число:");
while (b !== 0) {
    let temp = b;
    b = a % b;
    a = temp;
}
console.log(`Наибольший общий делитель:`, a);



