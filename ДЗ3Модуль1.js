// Задание 6: Зациклить калькулятор. Запросить у пользователя 2 числа и знак, решить пример, 
// вывести результат и спросить, хочет ли он решить еще один пример. И так до тех пор, пока 
// пользователь не откажется.

// Используем цикл do while, так как хотим, чтобы хотя бы один раз выполнился код внутри цикла
do {
    let num1 = +prompt("Введите первое число:");
    let num2 = +prompt("Введите второе число:");
    let operator = prompt("Введите оператор (+, -, *, /):");
    let result;
    switch (operator) {
        case '+':
            result = num1 + num2;
            break;
        case '-':
            result = num1 - num2;
            break;
        case '*':
            result = num1 * num2;
            break;
        case '/':
            result = num1 / num2;
            break;
        default:
            alert("Неверный оператор!");
            continue;
    }
    console.log(`Результат: ${result}`);
    let repeat = confirm("Хотите продолжить?");
} while (repeat);


// Задание 7: Запросить у пользователя число и на сколько цифр его сдвинуть. 
// Сдвинуть цифрычисла и вывести результат (если число 123456 сдвинуть на 2 цифры, то получится 345612).

// Используем цикл while для обработки сдвигов
let number = +prompt("Введите число:");
let shift = +prompt("На сколько цифр сдвинуть?");
let strNum = String(number);
while (shift-- > 0) {
    let lastDigit = strNum[strNum.length - 1];
    strNum = lastDigit + strNum.slice(0, -1);
}
console.log(`Полученное число после сдвига:`, Number(strNum));

