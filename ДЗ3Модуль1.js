// Задание 10: Игра «Угадай число». Предложить пользователю загадать число от 0 до 100 и 
// отгадать его следующим способом: каждую итерацию цикла делите диапазон чисел пополам, 
// записываете результат в N и спрашиваете у пользователя «Ваше число > N, < N или == N?». 
// В зависимости от того что указал пользователь, уменьшаете диапазон. Начальный диапазон от 0 до 100, 
// поделили пополам и получили 50. Если пользователь указал, что его число > 50, то изменили 
// диапазон на от 51 до 100. И так до тех пор, пока пользователь не выберет == N.

// Для этой игры подойдет цикл while, так как количество попыток заранее неизвестно и 
// игра продолжается до нахождения правильного числа.

// Диапазон чисел
let min = 0;
let max = 100;
let guess = Math.floor((min + max) / 2); // Начинаем с середины диапазона

console.log("Задумайте число от 0 до 100.");

while (true) {
    let answer = prompt(`Ваше число больше (${guess}), меньше (<${guess}) или равно (==${guess})? Введите символ: >, < или ==.`);
    
    if (answer === '>') {
        min = guess + 1; // Если число больше, меняем нижний предел
    } else if (answer === '<') {
        max = guess - 1; // Если число меньше, меняем верхний предел
    } else if (answer === '==') {
        alert(`Отлично! Вы угадали число: ${guess}.`); // Правильное число найдено
        break; // Завершаем игру
    } else {
        alert("Неправильный ввод. Попробуйте снова.");
    }
    
    guess = Math.floor((min + max) / 2); // Обновляем предположение
}
